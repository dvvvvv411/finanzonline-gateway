type Ctx = {
  lang: EstvLang;
  setLang: (l: EstvLang) => void;
  t: (key: string) => string;
};

const EstvI18nContext = createContext<Ctx | null>(null);

export const EstvI18nProvider = ({ children }: { children: ReactNode }) => {
  const [lang, setLang] = useState<EstvLang>("de");
  const t = (key: string) => translations[lang][key] ?? translations.de[key] ?? key;
  return (
    <EstvI18nContext.Provider value={{ lang, setLang, t }}>{children}</EstvI18nContext.Provider>
  );
};

export const useEstvI18n = (): Ctx => {
  const ctx = useContext(EstvI18nContext);
  if (!ctx) {
    // Fallback to DE if used outside provider
    return {
      lang: "de",
      setLang: () => {},
      t: (key: string) => translations.de[key] ?? key,
    };
  }
  return ctx;
};

